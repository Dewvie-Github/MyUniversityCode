num1= float(input("Enter value number 1 : "))
num2= float(input("Enter value number 2 : "))
num3= float(input("Enter value number 3 : "))

MinValue = min(num1,num2,num3)
MaxValue = max(num1,num2,num3)

print()
print ("Min value = ",MinValue,", Max value = ",MaxValue)
